// 1. 定义导航目的地 (屏幕)
// 使用密封类来定义每个屏幕，包含路由、标签和图标
sealed class Screen(val route: String, val label: String, val icon: ImageVector) {
    object Home : Screen("home", "首页", Icons.Filled.Home)
    object Search : Screen("search", "搜索", Icons.Filled.Search)
    object User : Screen("user", "我的", Icons.Filled.Person) // 使用 Category 图标
}

// 2. 创建包含所有屏幕的列表
val items = listOf(
    Screen.Home,
    Screen.Search,
    Screen.User,
)

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        // 启用沉浸式布局 (Edge-to-Edge)，建议在 setContent 之前调用
        enableEdgeToEdge()
        setContent {
            // 如果你有自定义主题，最好将 App() 包裹在主题中
            // YourTheme { App() }
            App()
        }
    }
}

@Composable
private fun App() {
    // 3. 创建导航控制器
    val navController = rememberNavController()

    // 4. 使用 Scaffold 构建基本布局
    Scaffold(
        bottomBar = {
            // 5. 在 Scaffold 的 bottomBar 中创建 NavigationBar
            NavigationBar {
                // 获取当前的导航栈条目状态
                val navBackStackEntry by navController.currentBackStackEntryAsState()
                // 获取当前的目的地
                val currentDestination = navBackStackEntry?.destination

                // 6. 遍历所有屏幕，为每个屏幕创建一个 NavigationBarItem
                items.forEach { screen ->
                    NavigationBarItem(
                        icon = { Icon(screen.icon, contentDescription = screen.label) }, // 设置图标
                        label = { Text(screen.label) }, // 设置标签文字
                        selected = currentDestination?.hierarchy?.any { it.route == screen.route } == true, // 判断是否选中
                        onClick = {
                            // 7. 点击时导航到对应的路由
                            navController.navigate(screen.route) {
                                // 弹出到导航图的起始目的地，避免在后退栈中堆积大量目的地
                                popUpTo(navController.graph.findStartDestination().id) {
                                    saveState = true // 保存状态
                                }
                                // 避免重复创建同一个目的地的多个实例
                                launchSingleTop = true
                                // 重新选择先前选中的项目时恢复状态
                                restoreState = true
                            }
                        }
                    )
                }
            }
        }
    ) { innerPadding -> // Scaffold 提供内边距，用于主内容区域
        // 8. 使用 NavHost 作为主内容区域，根据路由显示不同屏幕
        NavHost(
            navController = navController,
            startDestination = Screen.Home.route, // 设置起始页为首页
            modifier = Modifier
                .padding(innerPadding) // 应用 Scaffold 提供的内边距，避免内容与导航栏重叠
                .fillMaxSize() // 让内容区域充满可用空间
        ) {
            // 9. 定义每个路由对应的 Composable 界面
            composable(Screen.Home.route) { HomeScreen() }
            composable(Screen.Search.route) { SearchScreen() }
            composable(Screen.User.route) { UserScreen() }
        }
    }
}

